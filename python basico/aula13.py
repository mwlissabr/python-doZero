# Operadores de comparação
operador = 10 == 10
operador2 = 10 == 11
print(operador)
print(operador2)

operador3 = 'banana' != 'banana'
print(operador3)

operador4 = 10 < 100 
print(operador4)

operador5 = 10 > 100
print(operador5)

operador6 = 10 <= 10
print(operador6)

operador7 = 10 >= 20
print(operador7)





#     == Equal
#     != Not equal
#     >  Greater than
#     <  Less than
#     >= Greater than or equal to
#     <= Less than or equal to