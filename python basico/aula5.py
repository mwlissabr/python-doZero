# Aula sobre modificar os tipos de dados

# Mudando o número de integer para string utilizando a abreviação
x = str(3)

# Definindo que essa variável é um número inteiro
y = int(4)

# Definindo a variável como fração
z = float(5)

# Somando as variáveis para ver seu comportamento
print(x + x)
print(y + y)
print(z + z)